package part1.array;

public class StringArrayDemo2 {
	public static void main(String[] args) {
		
		String[] stringArr = {"1","2","3","4"};
		
		for(int i = 0; i<stringArr.length; i++){
			System.out.print(stringArr[i] + "\t");
		}
		
		System.out.println();
		
		int[] intArr = {1,2,3,4};
		
		for(int i = 0; i<intArr.length; i++){
			System.out.print(intArr[i] + "\t");
		}
	}
}
